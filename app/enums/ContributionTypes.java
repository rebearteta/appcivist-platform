package enums;

public enum ContributionTypes {
	ISSUE, // represents a problem to solve
	QUESTION, // represents a question to answer
	IDEA, // represents an idea for a proposal
	COMMENT, // represents a comment in a discussion or another contribution
	PROPOSAL, // represents a project proposal
	ASSESSMENT, // represents the technical assessment for a proposal
	FORUM_POST, // represents a post in the forum
	ACTION_ITEM, // represents a comment with a an action to realize in a due date
	DISCUSSION,  // represents a set of comments related to a topic
	BRAINSTORMING, // represents a contribution to the brainstorming phase
	DELIBERATIVE_DISCUSSION, // represents a discussion that must have one or more decisions at the end
	NOTE, 
}
